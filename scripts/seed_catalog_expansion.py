def score_inputs(sku_id: str, category: str, product_type: str) -> dict:
    seed = int(hashlib.sha256(sku_id.encode()).hexdigest()[:8], 16)
    category_bonus = {"纸品/湿巾": 0.3, "衣物清洁": 0.2, "家庭清洁": 0.1, "个护日化": 0.0}[category]
    type_profit = {"引流款": 7.4, "平销款": 8.0, "利润款": 8.6}[product_type]

    def jitter(offset: int, base: float) -> float:
        return round(min(9.2, max(6.8, base + ((seed >> offset) % 7 - 3) * 0.12)), 1)

    return {
        "platformHeat": jitter(0, 8.0 + category_bonus),
        "priceCompetitiveness": jitter(3, 8.0),
        "profitFeasibility": jitter(6, type_profit),
        "salesProof": jitter(9, 7.8),
        "repeatPurchase": jitter(12, 8.6 if category != "个护日化" else 8.1),
        "differentiation": jitter(15, 7.8),
        "operability": jitter(18, 8.1),
        "riskPenalty": round(0.5 + (seed % 5) * 0.1, 1),
    }


