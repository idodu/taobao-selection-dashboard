def main() -> None:
    catalog = json.loads(CATALOG_PATH.read_text(encoding="utf-8"))
    existing = {item["sourceSkuId"]: item for item in catalog.get("products", [])}
    for candidate in CANDIDATES:
        product = build_product(candidate)
        existing.setdefault(product["sourceSkuId"], product)

    products = list(existing.values())
    if len(products) != 40:
        raise RuntimeError(f"expected 40 catalog products, got {len(products)}")

    catalog["catalogVersion"] = "2026-06-14-single-sku-v3-rotation"
    catalog["products"] = products
    CATALOG_PATH.write_text(
        json.dumps(catalog, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"catalog expanded to {len(products)} concrete SKUs")


if __name__ == "__main__":
    main()
